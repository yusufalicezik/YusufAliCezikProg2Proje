    private void guncelleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleButtonActionPerformed
          
        int a=akademisyenTablos.getSelectedRow();
        guncelleDialog.setVisible(true);
        ArrayList bilgilerListe=ay.bilgileriAl(a); //güncelleye bastığımda seçtiğim tablo row sırasındaki bilgilerin
                                                   //güncelleme ekranındaki edittextlerde gözükmesi için...
        
        guncelleIDNo.setText(bilgilerListe.get(0).toString());
        guncelleSicilNo.setText(bilgilerListe.get(1).toString());
        guncelleAdSoyad.setText(bilgilerListe.get(2).toString());
        guncelleSehir.setText(bilgilerListe.get(3).toString());
        guncelleTel.setText(bilgilerListe.get(4).toString());
        
      
        
    }//GEN-LAST:event_guncelleButtonActionPerformed

    private void guncelleSaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guncelleSaveButtonActionPerformed
     
        int secilenRow=akademisyenTablos.getSelectedRow();
        
      ay.guncelle(guncelleIDNo.getText(),guncelleSicilNo.getText(),guncelleAdSoyad.getText(),guncelleSehir.getText(),guncelleTel.getText(),secilenRow);
     int a=JOptionPane.showConfirmDialog(this,"Seçtiğiniz kayıt güncellendi.","Bilgi",JOptionPane.DEFAULT_OPTION);
    if(a==0)
    {
        guncelleDialog.setVisible(false);
    }
   
      
    }//GEN-LAST:event_guncelleSaveButtonActionPerformed

