    private void kaydetEkleButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kaydetEkleButtonActionPerformed
      int a=JOptionPane.showConfirmDialog(this,"Kayıt eklendi","Bilgi",JOptionPane.DEFAULT_OPTION);   
      if(a==0)
      { int id=Integer.valueOf(kaydetIDGiris.getText());
       String sicilNo=kaydetSicilNoGiris.getText();
       String adSoyad=kaydetAdSoyadGiris.getText();
       String sehir=kaydetSehirGiris.getText();
       String tel=kaydetTelGiris.getText();
        ay.yeniKayitEkle(id,sicilNo,adSoyad,sehir,tel);
         kayiteklemeDialog.setVisible(false);
      }
       
       
      
       
       
       
           
    }//GEN-LAST:event_kaydetEkleButtonActionPerformed

